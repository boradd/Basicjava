package lecturethree;

public class incrementDrement {
    public static void main(String[] args) {
        // Increment Decrement

        int a = 6;
        System.out.println(a);
        a++;
        System.out.println(a);
        a=a+3;
        a=a-2;
        System.out.println(a);

        System.out.println(a);
        int b= 7;
        System.out.println(b);
        b--;
        System.out.println(b);
    }
}
