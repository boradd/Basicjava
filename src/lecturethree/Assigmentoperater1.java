package lecturethree;

public class Assigmentoperater1 {
    public static void main(String[] args) {

        // Assigment
        int a=5;
        System.out.println(a);

        a=10;
        System.out.println(a);

        // +=

        a+=20;
        System.out.println(a);

        // -=

        a-=25;
        System.out.println(a);

        // *=
        a*=5;
        System.out.println(a);

        // /=
        a /= 10;
        System.out.println(a);









    }
}
