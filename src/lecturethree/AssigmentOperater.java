package lecturethree;

public class AssigmentOperater {

    public static void main(String[] args) {

        // assigment

        int a = 5; //a is a variable int datatype (=) is Assigment Operater 5 is a value
        a=10;
        // +=
        a+=10; // a= a+10
        System.out.println(a);

        // -=
        a-=5; // a=a-5 // a=a-5 (answer is 5 subtract)
        System.out.println(a);

        //*=
        a*=5; // a= a*5
        System.out.println(a);
        // /=

        a/=5; //a =a/5
        System.out.println(a);

        //%=
        a%=5; //a= %5
        System.out.println(a);







    }
}
